import { NextRequest } from 'next/server'
import { promises as fs } from 'fs'
import path from 'path'

// ── Session persistence ───────────────────────────────────────────────────────

type SessionMessage = { role: string; content: string; time?: string }
type LeadScore = { feasibility: number; impact: number; urgency: number; overall: number }

function sanitizeSessionId(id: unknown): string | null {
  if (typeof id !== 'string') return null
  return /^[a-zA-Z0-9_-]{8,64}$/.test(id) ? id : null
}

function buildMarkdown(
  sessionId: string,
  startedAt: string,
  updatedAt: string,
  messages: SessionMessage[],
  leadScore: LeadScore | null,
): string {
  const status = leadScore ? 'completed' : 'in-progress'

  const conversation = messages
    .map(m => {
      const speaker = m.role === 'user' ? '**User**' : '**Bob**'
      const time = m.time ? ` *(${m.time})*` : ''
      return `${speaker}${time}\n> ${m.content.replace(/\n/g, '\n> ')}`
    })
    .join('\n\n')

  const scoreRows = leadScore
    ? `| Feasibility | ${leadScore.feasibility}/10 |\n| Impact | ${leadScore.impact}/10 |\n| Urgency | ${leadScore.urgency}/10 |\n| **Overall** | **${leadScore.overall}/10** |`
    : `| Feasibility | — |\n| Impact | — |\n| Urgency | — |\n| **Overall** | **—** |`

  return `# Sowedo Intake Session

**Session ID:** \`${sessionId}\`
**Started:** ${startedAt}
**Last Updated:** ${updatedAt}
**Status:** \`${status}\`

---

## Conversation

${conversation}

---

## Lead Score

| Dimension | Score |
|-----------|-------|
${scoreRows}

---

_Generated by Sowedo AI Agent (Bob)_
`
}

async function saveSession(
  sessionId: string,
  messages: SessionMessage[],
  leadScore: LeadScore | null,
): Promise<void> {
  try {
    const dir = path.join(process.cwd(), 'sessions')
    await fs.mkdir(dir, { recursive: true })
    const filePath = path.join(dir, `${sessionId}.md`)
    const now = new Date().toLocaleString('nl-NL', { timeZone: 'Europe/Amsterdam' })

    // Preserve the original startedAt from an existing file
    let startedAt = now
    try {
      const existing = await fs.readFile(filePath, 'utf-8')
      const m = existing.match(/\*\*Started:\*\* (.+)/)
      if (m) startedAt = m[1].trim()
    } catch {
      // First write for this session
    }

    await fs.writeFile(filePath, buildMarkdown(sessionId, startedAt, now, messages, leadScore), 'utf-8')
  } catch (err) {
    console.error('[session] Failed to save:', err)
    // Never throw — session saving must not break the chat
  }
}

